from collections import Counter#импортируем counter
str1 = input("Введите первую строку: ")#запрашиваем ввод первой строки
str2 = input("Введите вторую строку: ")#запрашиваем ввод второй строки
if Counter(str1) == Counter(str2):#создаем условие анаграммы
    print("Это анаграммы")#выводим ответ 
else:#создаем другое словие
    print("Это не анаграммы")#
